print('첫 번째 정수를 입력하세요.')
number1 = int(input())

print('두 번째 정수를 입력하세요.')
number2 = int(input())

print(number1, '+', number2, '=', number1 + number2)
print(number1, '-', number2, '=', number1 - number2)
print(number1, '*', number2, '=', number1 * number2)
print(number1, '/', number2, '=', number1 / number2)


